// Program ini mengonversi suhu dari Celcius ke Fahrenheit dan sebaliknya.
// Tujuan: Melatih penggunaan `let`, `const`, tipe data number, dan operator aritmatika.

// This program converts temperatures from Celsius to Fahrenheit and vice versa.
// Objective: To practice using `let`, `const`, number data types, and arithmetic operators.

// Rumus konversi:
// Conversion formulas:
// F = (C × 9/5) + 32
// C = (F - 32) × 5/9

// Konversi Celcius ke Fahrenheit
// Conversion from Celsius to Fahrenheit
let celcius = 25;
let fahrenheit = (celcius * 9 / 5) + 32;
console.log(celcius + "°C = " + fahrenheit + "°F");

// Konversi Fahrenheit ke Celcius
// Conversion from Fahrenheit to Celsius
let fahrenheit2 = 77;
let celcius2 = (fahrenheit2 - 32) * 5 / 9;
console.log(fahrenheit2 + "°F = " + celcius2 + "°C");
